<script setup>
import { ref, reactive, onMounted, computed } from 'vue'
import GlassmorphicCard from './GlassmorphicCard.vue'
import { ElMessage, ElLoading } from 'element-plus'
import { EditPen, ArrowLeft } from '@element-plus/icons-vue'
import { userState, userActions } from '@/utils/userStore'
import { API_SERVER_URL } from '@/utils/urlutils'
import { userAPI } from '@/utils/apiservice'

// Êé•Êî∂‰ªéÁà∂ÁªÑ‰ª∂‰º†Êù•ÁöÑprops
const props = defineProps({
  isDarkMode: {
    type: Boolean,
    default: true,
  },
  toggleTheme: {
    type: Function,
    required: true,
  },
})

// ÂÆö‰πâ‰∫ã‰ª∂
const emit = defineEmits(['back'])

// ÂìçÂ∫îÂºèÊï∞ÊçÆ
const fileInputRef = ref(null)
const profileFormRef = ref(null)
const updating = ref(false)
const uploadingAvatar = ref(false)
const currentAvatar = ref('')

// Ë°®ÂçïÊï∞ÊçÆ
const profileForm = reactive({
  username: '',
  email: '',
  nickname: '',
})

// Áî®Êà∑‰ø°ÊÅØ
const userInfo = computed(() => userState.userInfo)

// ÈªòËÆ§Â§¥ÂÉè
const defaultAvatarUrl = computed(() => {
  return '/default-avatar.png'
})

// Ë°®ÂçïÈ™åËØÅËßÑÂàô
const rules = {
  nickname: [
    { max: 50, message: 'ÊòµÁß∞ÈïøÂ∫¶‰∏çËÉΩË∂ÖËøá50‰∏™Â≠óÁ¨¶', trigger: 'blur' },
  ],
}

// ÂàùÂßãÂåñÁî®Êà∑‰ø°ÊÅØ
const initUserInfo = async () => {
  // È¶ñÂÖàÂ∞ùËØïËé∑ÂèñÊúÄÊñ∞ÁöÑÁî®Êà∑‰ø°ÊÅØ
  await refreshUserProfile()
  
  if (userInfo.value) {
    profileForm.username = userInfo.value.username || ''
    profileForm.email = userInfo.value.email || ''
    profileForm.nickname = userInfo.value.nickname || userInfo.value.username || ''
    
    // ËÆæÁΩÆÂ§¥ÂÉèURL
    updateAvatarUrl()
  }
}

// Êõ¥Êñ∞Â§¥ÂÉèURLÁöÑÁªü‰∏ÄÊñπÊ≥ï
const updateAvatarUrl = () => {
  if (userInfo.value?.avatar_url || userInfo.value?.avatarUrl) {
    // ‰ºòÂÖà‰ΩøÁî®avatar_urlÔºåÂ¶ÇÊûúÊ≤°ÊúâÂàô‰ΩøÁî®avatarUrl
    const avatarPath = userInfo.value.avatar_url || userInfo.value.avatarUrl
    
    // Â¶ÇÊûúÂ∑≤ÁªèÊòØÂÆåÊï¥URLÔºåÁõ¥Êé•‰ΩøÁî®
    if (avatarPath.startsWith('http')) {
      currentAvatar.value = avatarPath
    } else {
      // Âê¶ÂàôÊãºÊé•ÂÆåÊï¥URL
      currentAvatar.value = `${API_SERVER_URL}${avatarPath}`
    }
  } else {
    currentAvatar.value = defaultAvatarUrl.value
  }
}

// Âà∑Êñ∞Áî®Êà∑ËµÑÊñô‰ø°ÊÅØ
const refreshUserProfile = async () => {
  try {
    const result = await userActions.getUserProfile()
    if (result.success) {
      // Êõ¥Êñ∞Â§¥ÂÉèURL
      updateAvatarUrl()
    }
  } catch (error) {
    console.error('Ëé∑ÂèñÁî®Êà∑ËµÑÊñôÂ§±Ë¥•:', error)
  }
}

// Â§ÑÁêÜ‰∏ªÈ¢òÂàáÊç¢
const handleToggleTheme = () => {
  props.toggleTheme()
}

// Ëß¶ÂèëÊñá‰ª∂ÈÄâÊã©
const triggerFileInput = () => {
  fileInputRef.value?.click()
}

// Â§ÑÁêÜÂ§¥ÂÉèÊñá‰ª∂ÈÄâÊã©
const handleAvatarChange = async (event) => {
  const file = event.target.files[0]
  if (!file) return

  // È™åËØÅÊñá‰ª∂Á±ªÂûã
  const allowedTypes = ['image/jpeg', 'image/jpg', 'image/png']
  if (!allowedTypes.includes(file.type)) {
    ElMessage.error('ËØ∑ÈÄâÊã© JPG Êàñ PNG Ê†ºÂºèÁöÑÂõæÁâá')
    return
  }

  // È™åËØÅÊñá‰ª∂Â§ßÂ∞è (2MB)
  if (file.size > 2 * 1024 * 1024) {
    ElMessage.error('ÂõæÁâáÂ§ßÂ∞è‰∏çËÉΩË∂ÖËøá 2MB')
    return
  }

  // È¢ÑËßàÂ§¥ÂÉè
  const reader = new FileReader()
  reader.onload = (e) => {
    currentAvatar.value = e.target.result
  }
  reader.readAsDataURL(file)

  // ‰∏ä‰º†Â§¥ÂÉè
  await uploadAvatar(file)
}

// ‰∏ä‰º†Â§¥ÂÉè
const uploadAvatar = async (file) => {
  uploadingAvatar.value = true
  
  try {
    const formData = new FormData()
    formData.append('avatar', file)

    const response = await userAPI.uploadAvatar(formData)
    const result = response.data

    if (result.status === 'success') {
      ElMessage.success('Â§¥ÂÉèÊõ¥Êñ∞ÊàêÂäü')
      
      // Êõ¥Êñ∞Áî®Êà∑‰ø°ÊÅØ‰∏≠ÁöÑÂ§¥ÂÉèURL
      if (userState.userInfo) {
        // ‰ΩøÁî®ÂêéÁ´ØËøîÂõûÁöÑÂ§¥ÂÉèURLÔºå‰ºòÂÖà‰ΩøÁî®avatar_urlÂ≠óÊÆµ
        userState.userInfo.avatar_url = result.data.avatar_url || result.data.avatarUrl
        userState.userInfo.avatarUrl = result.data.avatar_url || result.data.avatarUrl
        
        // Êõ¥Êñ∞Êú¨Âú∞Â≠òÂÇ®
        const storage = localStorage.getItem('auth_token') ? localStorage : sessionStorage
        storage.setItem('user_info', JSON.stringify(userState.userInfo))
        
        // Êõ¥Êñ∞ÂΩìÂâçÊòæÁ§∫ÁöÑÂ§¥ÂÉè
        updateAvatarUrl()
      }
    } else {
      throw new Error(result.message || 'Â§¥ÂÉè‰∏ä‰º†Â§±Ë¥•')
    }
  } catch (error) {
    console.error('Â§¥ÂÉè‰∏ä‰º†Â§±Ë¥•:', error)
    
    let errorMessage = 'Â§¥ÂÉè‰∏ä‰º†Â§±Ë¥•ÔºåËØ∑ÈáçËØï'
    if (error.response?.data?.message) {
      errorMessage = error.response.data.message
    } else if (error.message) {
      errorMessage = error.message
    }
    
    ElMessage.error(errorMessage)
    
    // ÊÅ¢Â§çÂéüÂ§¥ÂÉè
    updateAvatarUrl()
  } finally {
    uploadingAvatar.value = false
    // Ê∏ÖÁ©∫Êñá‰ª∂ËæìÂÖ•Ê°Ü
    if (fileInputRef.value) {
      fileInputRef.value.value = ''
    }
  }
}

// Â§ÑÁêÜÂ§¥ÂÉèÂä†ËΩΩÈîôËØØ
const handleAvatarError = () => {
  currentAvatar.value = defaultAvatarUrl.value
}

// Êõ¥Êñ∞‰∏™‰∫∫‰ø°ÊÅØ
const handleUpdateProfile = async () => {
  if (!profileFormRef.value) return

  try {
    const valid = await profileFormRef.value.validate()
    if (!valid) return

    updating.value = true

    // ËøôÈáåÂèØ‰ª•Ê∑ªÂä†Êõ¥Êñ∞‰∏™‰∫∫‰ø°ÊÅØÁöÑAPIË∞ÉÁî®
    // ÁõÆÂâçÂè™Êõ¥Êñ∞ÊòµÁß∞ÔºåÂõ†‰∏∫Áî®Êà∑ÂêçÂíåÈÇÆÁÆ±ÈÄöÂ∏∏‰∏çÂÖÅËÆ∏‰øÆÊîπ
    const updateData = {
      nickname: profileForm.nickname,
    }

    // Ê®°ÊãüAPIË∞ÉÁî®
    await new Promise(resolve => setTimeout(resolve, 1000))
    
    ElMessage.success('‰∏™‰∫∫‰ø°ÊÅØÊõ¥Êñ∞ÊàêÂäü')
    
    // Êõ¥Êñ∞Êú¨Âú∞Áî®Êà∑‰ø°ÊÅØ
    if (userState.userInfo) {
      userState.userInfo.nickname = profileForm.nickname
      
      // Êõ¥Êñ∞Êú¨Âú∞Â≠òÂÇ®
      const storage = localStorage.getItem('auth_token') ? localStorage : sessionStorage
      storage.setItem('user_info', JSON.stringify(userState.userInfo))
    }
  } catch (error) {
    console.error('Êõ¥Êñ∞‰∏™‰∫∫‰ø°ÊÅØÂ§±Ë¥•:', error)
    ElMessage.error('Êõ¥Êñ∞Â§±Ë¥•ÔºåËØ∑ÈáçËØï')
  } finally {
    updating.value = false
  }
}

// ËøîÂõû‰∏ªÈ°µÈù¢
const handleBack = () => {
  emit('back')
}

// ÁªÑ‰ª∂ÊåÇËΩΩÊó∂ÂàùÂßãÂåñ
onMounted(async () => {
  await initUserInfo()
})
